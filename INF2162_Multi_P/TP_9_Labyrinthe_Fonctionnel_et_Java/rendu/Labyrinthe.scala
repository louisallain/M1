class Labyrinthe(init_ : Array[Array[Int]]) {

  val init = init_
  val coordsDebut: Tuple2[Int, Int] = (0, 1)

  /**
   * 0 : case mur
   * 1 : case possible
   * 2 : case solution
   */
  override def toString = {

    this.init.map(ligne => ligne.map(cCase => {
      cCase match {
        case(1) => s"${YELLOW_B}   ${RESET}"
        case(2) => s"${CYAN_B}   ${RESET}"
        case(9) => s"${RED_B}   ${RESET}"
        case(cCase) => s"${BLACK_B}   ${RESET}"
      }
    }).mkString("")).mkString("\n")
  }

  def cheminSortieAPartirDe(pos_ : Tuple2[Int, Int], lab_ :  Array[Array[Int]] = init_) : Set[Array[Array[Int]]] = {

    lab_(pos_._1)(pos_._2) match {

      case 9 => Set(lab_)
      case 0 =>{

        val labMarque = lab_.updated(pos_._1, lab_(pos_._1).updated(pos_._2, 2))// marque notre passage
        val voisins = List((pos_._1, pos_._2 - 1), (pos_._1 - 1, pos_._2), (pos_._1, pos_._2 + 1), (pos_._1 + 1, pos_._2)).filter(_._1 >= 0).filter(_._2 >= 0).filter(_._1 < labMarque.length).filter(_._2 < labMarque(0).length)

        voisins.map(voisin => cheminSortieAPartirDe(voisin, labMarque)).flatten.toSet
      }
      case 1 | 2 => Set()
    }
  }
}

